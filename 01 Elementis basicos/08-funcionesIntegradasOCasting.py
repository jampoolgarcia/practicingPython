# integrated functions

# sting to number
print("sting to number")
n = int("10")
print(n)

# sting to float
print("sting to float")
n = float("10.8")
print(n)

# number to string
print("number to string")
n = str(10)
print(n)

# float to string
print("float to string")
n = str(10.8)
print(n)

# number to binary
print("number to binary")
n = bin(10)
print(n)

# number to hexadecimal
print("number to hexadecimal")
n = hex(10)
print(n)

# binary to number
print("binary to number")
n = int("0b1010", 2)
print(n)

# hexadecimal to number
print("hexadecimal to number")
n = int("0xa", 16)
print(n)

# Absolutely value of a number
print("Absolutely value of a number")
n = abs(-8)
print(n)


# round number
print("round number")
n = round(3.6)
print(n)
n = round(3.4)
print(n)

# length
print("length")
n = len("Hello word")
print(n)
